package com.lifegoals.app.context;

public class HttpHelperResponse {
	private String content;
	private int code;

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

}
